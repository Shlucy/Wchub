const formatDate = (value) =>
  new Intl.DateTimeFormat("en-US", { month: "long", day: "numeric", year: "numeric" }).format(
    new Date(`${value}T12:00:00`)
  );

const getPriceRange = (stage) => {
  if (stage === "Final") return "Category 1: $1,200 - $2,000";
  if (stage === "Semi-finals") return "Category 1: $850 - $1,600";
  if (stage === "Quarter-finals") return "Category 1: $650 - $1,300";
  if (stage === "Round of 16" || stage === "Round of 32") return "Category 1: $550 - $1,100";
  return "Category 1: $250 - $950";
};

const getPriceBounds = (stage) => {
  if (stage === "Final") return { min: 1200, max: 2000 };
  if (stage === "Semi-finals") return { min: 850, max: 1600 };
  if (stage === "Quarter-finals") return { min: 650, max: 1300 };
  if (stage === "Round of 16" || stage === "Round of 32") return { min: 550, max: 1100 };
  return { min: 250, max: 950 };
};

